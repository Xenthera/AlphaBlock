package com.bobby.blocks.texture;

public class TextureAtlasPosition {

    public float minX, minY, maxX, maxY;

    public TextureAtlasPosition(float minX, float minY, float maxX, float maxY){
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }
}
